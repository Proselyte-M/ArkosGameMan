def tr(lang: str, key: str, **kwargs) -> str:
    value = I18N.get(lang, {}).get(key)
    if value is None and lang != "en":
        value = I18N.get("en", {}).get(key)
    if value is None:
        value = I18N[DEFAULT_LANG].get(key, key)
    if kwargs:
        try:
            return value.format(**kwargs)
        except Exception:
            return value
    return value
